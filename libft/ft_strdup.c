#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		str_len;
	char	*str;

	i = 0;
	str_len = ft_strlen(s1);
	str = (char *)malloc(sizeof(char) * (str_len + 1));
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
